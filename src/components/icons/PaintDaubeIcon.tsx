interface PaintDaubeIconProps {
  variant: IconVariant;
  color?: IconColor;
  size?: number;
  /** Intensity of the turbulence effect (0-1) */
  turbulence?: number;
  /** Unique seed for procedural variation */
  seed?: number;
}

/**
 * Generates an organic path with slight imperfections
 * Uses seed for reproducible randomness
 */
function generateOrganicOffset(seed: number, index: number, maxOffset: number = 1.5): number {
  // Simple seeded pseudo-random
  const x = Math.sin(seed * 9999 + index * 7919) * 10000;
  return (x - Math.floor(x) - 0.5) * maxOffset * 2;
}

/**
 * Paint Daube SVG Filter Definition
 * Creates the organic paint texture effect
 */
function PaintDaubeFilter({ id, turbulence }: { id: string; turbulence: number }) {
  return (
    <Defs>
      <Filter id={id} x="-20%" y="-20%" width="140%" height="140%">
        <FeTurbulence
          type="fractalNoise"
          baseFrequency={0.02 + turbulence * 0.03}
          numOctaves={2}
          result="noise"
        />
        <FeDisplacementMap
          in="SourceGraphic"
          in2="noise"
          scale={turbulence * 3}
          xChannelSelector="R"
          yChannelSelector="G"
        />
      </Filter>
    </Defs>
  );
}

